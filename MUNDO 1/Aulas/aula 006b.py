n = str(input('Digite um valor: '))

print(n.isnumeric()) # ele vai dizer se é possivel converter esse valor em número 
print(n.isalpha()) # vai infomar se é letra
print(n.isalnum()) # vai infomar número e letra na mesma palavra 
print(n.isupper()) # vai infomar letra maiúscula 
print(n.islower()) # vai infomar letra minúscula
print(n.isspace()) # vai infomar se é uma espaço 
